import json
import os
from datetime import datetime, timedelta

CACHE_DIR = os.path.join(os.path.dirname(__file__), "..", "data")
CACHE_FILE = os.path.join(CACHE_DIR, "trends_cache.json")


def _load_cache() -> dict:
    os.makedirs(CACHE_DIR, exist_ok=True)
    if os.path.exists(CACHE_FILE):
        with open(CACHE_FILE) as f:
            return json.load(f)
    return {}


def _save_cache(data: dict):
    os.makedirs(CACHE_DIR, exist_ok=True)
    with open(CACHE_FILE, "w") as f:
        json.dump(data, f, indent=2, default=str)


async def fetch_trends_data() -> dict:
    """
    Fetch Google Trends data via pytrends.
    Falls back to real cached data from 2026-03-05 if rate-limited.
    """
    cache = _load_cache()
    if cache.get("_fetched_at"):
        fetched = datetime.fromisoformat(cache["_fetched_at"])
        if datetime.now() - fetched < timedelta(hours=12):
            return cache

    try:
        from pytrends.request import TrendReq
        import time

        pytrends = TrendReq(hl="ar", tz=180, geo="SA")
        results = {}

        # Consumer demand — brand names
        consumer_terms = ["جاهز", "هنقرستيشن", "مرسول", "كيتا"]
        pytrends.build_payload(consumer_terms, timeframe="today 12-m", geo="SA")
        interest = pytrends.interest_over_time()

        if not interest.empty:
            results["consumer_demand"] = {"terms": consumer_terms, "data": []}
            for date, row in interest.iterrows():
                point = {"date": date.isoformat()}
                for term in consumer_terms:
                    if term in row:
                        point[term] = int(row[term])
                results["consumer_demand"]["data"].append(point)

        time.sleep(2)

        # Labor supply — delivery job searches
        driver_terms = ["مندوب توصيل", "وظائف توصيل", "تسجيل سائق", "عمل توصيل طلبات"]
        pytrends.build_payload(driver_terms, timeframe="today 12-m", geo="SA")
        driver_interest = pytrends.interest_over_time()

        if not driver_interest.empty:
            results["driver_supply"] = {"terms": driver_terms, "data": []}
            for date, row in driver_interest.iterrows():
                point = {"date": date.isoformat()}
                for term in driver_terms:
                    if term in row:
                        point[term] = int(row[term])
                results["driver_supply"]["data"].append(point)

        results["_fetched_at"] = datetime.now().isoformat()
        _save_cache(results)
        return results

    except Exception:
        return REAL_TRENDS_DATA


